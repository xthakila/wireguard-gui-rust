/// Build the complete `nft -f` script text that installs the server masquerade
/// ruleset.
///
/// The returned string is a self-contained nftables script that:
/// - Creates `table inet wg_gui_srv_nat` with a `nat hook postrouting` chain
/// - Adds `ip saddr <subnet> oifname "<egress_iface>" masquerade`
///
/// # Parameters
///
/// - `subnet`        — the tunnel subnet to masquerade (e.g. `"10.7.0.0/24"`)
/// - `egress_iface`  — the host's internet-facing interface (e.g. `"eth0"`)
///
/// # Guarantee
///
/// This function is **pure** and never executes any command.  Pass the output to
/// a temp file and invoke `nft -c -f <file>` (syntax check only) or let the
/// helper run `nft -f <file>` to apply.
pub fn build_nat_ruleset(subnet: &str, egress_iface: &str) -> String {
    format!(
        "# wg_gui_srv_nat — generated by wireguard-gui-rust\n\
         # DO NOT EDIT MANUALLY — managed by wireguard-gui-helper\n\
         \n\
         table inet wg_gui_srv_nat {{\n\
             chain postrouting {{\n\
                 type nat hook postrouting priority 100;\n\
                 ip saddr {subnet} oifname \"{egress_iface}\" masquerade;\n\
             }}\n\
         }}\n"
    )
}

/// Build the `sysctl -w net.ipv4.ip_forward=1` argv.
///
/// Pure.  The helper executes this; the GUI only builds it (e.g. for golden
/// tests or dry-run previews).
pub fn sysctl_ipv4_forward_argv() -> Vec<String> {
    vec![
        "sysctl".into(),
        "-w".into(),
        "net.ipv4.ip_forward=1".into(),
    ]
}

/// Build the `sysctl -w net.ipv6.conf.all.forwarding=1` argv.
///
/// Pure.  Applied best-effort in the helper (non-fatal if IPv6 is absent).
pub fn sysctl_ipv6_forward_argv() -> Vec<String> {
    vec![
        "sysctl".into(),
        "-w".into(),
        "net.ipv6.conf.all.forwarding=1".into(),
    ]
}

/// Build the `nft delete table inet wg_gui_srv_nat` argv.
///
/// Family and table name are SEPARATE arguments — mirrors the kill-switch pattern
/// and matches how `nft` parses its command line.  Pass `argv[0]` as the program
/// and `argv[1..]` as the args.
pub fn nft_delete_nat_table_argv() -> Vec<String> {
    vec![
        "nft".into(),
        "delete".into(),
        "table".into(),
        "inet".into(),
        "wg_gui_srv_nat".into(),
    ]
}

// ─────────────────────────────────────────────────────────────────────────────
// Client-side helpers — build and dispatch PrivCmd values
// ─────────────────────────────────────────────────────────────────────────────

/// GUI-side server NAT controller.
///
/// All methods are **pure builders or async dispatchers**.  The actual sysctl /
/// nft execution happens only inside the root helper binary (via `pkexec`); this
/// struct never runs privileged code.
pub struct ServerNat;

impl ServerNat {
    /// Enable NAT masquerade for `subnet` out `egress_iface`.
    ///
    /// Dispatches [`PrivCmd::NatEnable`] through the privileged helper.  The helper
    /// will:
    ///   1. `sysctl -w net.ipv4.ip_forward=1`
    ///   2. `sysctl -w net.ipv6.conf.all.forwarding=1` (best-effort)
    ///   3. Apply the masquerade nft ruleset via `nft -f <tempfile>`.
    pub async fn enable(&self, subnet: &str, egress_iface: &str) -> AppResult<()> {
        let cmd = PrivCmd::NatEnable {
            subnet: subnet.to_owned(),
            egress_iface: egress_iface.to_owned(),
        };
        run_privileged(&cmd).await
    }

    /// Disable NAT masquerade: remove the `wg_gui_srv_nat` table.
    ///
    /// Dispatches [`PrivCmd::NatDisable`] through the privileged helper.  The helper
    /// runs `nft delete table inet wg_gui_srv_nat`.  Idempotent — if the table is
    /// already absent, the helper treats it as success.
    pub async fn disable(&self) -> AppResult<()> {
        run_privileged(&PrivCmd::NatDisable).await
    }

    /// Build the [`PrivCmd::NatEnable`] payload **without dispatching it**.
    ///
    /// Use for golden-string tests, dry-run previews, or anywhere the JSON wire
    /// shape must be inspected without executing anything.
    pub fn enable_cmd(subnet: &str, egress_iface: &str) -> AppResult<String> {
        let cmd = PrivCmd::NatEnable {
            subnet: subnet.to_owned(),
            egress_iface: egress_iface.to_owned(),
        };
        encode(&cmd)
    }

    /// Build the [`PrivCmd::NatDisable`] payload without dispatching it.
    pub fn disable_cmd() -> AppResult<String> {
        encode(&PrivCmd::NatDisable)
    }

    /// Build the [`PrivCmd::ServerWriteConf`] payload without dispatching it.
    pub fn server_write_conf_cmd(conf_text: &str) -> AppResult<String> {
        let cmd = PrivCmd::ServerWriteConf {
            conf_text: conf_text.to_owned(),
        };
        encode(&cmd)
    }

    /// Build the [`PrivCmd::ServerUp`] payload without dispatching it.
    pub fn server_up_cmd() -> AppResult<String> {
        encode(&PrivCmd::ServerUp)
    }

    /// Build the [`PrivCmd::ServerDown`] payload without dispatching it.
    pub fn server_down_cmd() -> AppResult<String> {
        encode(&PrivCmd::ServerDown)
    }
}

