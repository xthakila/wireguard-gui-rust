/// The nftables table used exclusively for server masquerade NAT.
///
/// Uniquely named so it never collides with user-managed tables or the
/// kill-switch table.
pub const NFT_NAT_TABLE: &str = "inet wg_gui_srv_nat";

// ─────────────────────────────────────────────────────────────────────────────
