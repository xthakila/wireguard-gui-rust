//! Server-side NAT masquerade — pure builders and GUI-side dispatchers.
//!
//! # Design
//!
//! The server needs source NAT (masquerade) so tunnel clients can reach the internet
//! through this host.  The implementation uses a uniquely-named nftables table
//! (`inet wg_gui_srv_nat`) — the same unique-name pattern as the kill-switch — so it
//! never collides with any user-managed firewall table.
//!
//! ## What the NAT table does
//!
//! ```text
//! table inet wg_gui_srv_nat {
//!     chain postrouting {
//!         type nat hook postrouting priority 100;
//!         ip saddr <subnet> oifname "<egress_iface>" masquerade;
//!     }
//! }
//! ```
//!
//! All IPv4 traffic sourced from `subnet` (e.g. `10.7.0.0/24`) that leaves
//! `egress_iface` (e.g. `eth0`) is masqueraded as the host's own IP.  This lets
//! WireGuard clients reach the internet without static SNAT entries.
//!
//! ## IPv4 forwarding
//!
//! `NatEnable` also enables kernel IP forwarding:
//!   - `sysctl -w net.ipv4.ip_forward=1`
//!   - `sysctl -w net.ipv6.conf.all.forwarding=1` (best-effort)
//!
//! Forwarding is intentionally NOT disabled on `NatDisable` — other services
//! on the same host may depend on it being set.
//!
//! ## Privilege boundary
//!
//! This module is **pure GUI-side**.  It builds [`PrivCmd`] values and pure
//! ruleset / argv text; it never runs any privileged command.  The actual
//! `sysctl`, `nft -f`, and `nft delete table` invocations happen exclusively
//! inside the root `wireguard-gui-helper` binary.

use crate::error::AppResult;
use crate::net::privilege::{encode, run_privileged, PrivCmd};

