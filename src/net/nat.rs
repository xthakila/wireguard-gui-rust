    // ── nft syntax check (requires nft binary — mark #[ignore]) ──────────────

    fn nft_available() -> bool {
        Command::new("nft").arg("--version").output().is_ok()
    }

    fn nft_check_syntax(ruleset: &str) -> std::io::Result<bool> {
        use std::io::Write as _;
        let mut f = tempfile::NamedTempFile::new()?;
        f.write_all(ruleset.as_bytes())?;
        let status = Command::new("nft")
            .arg("-c")
            .arg("-f")
            .arg(f.path())
            .status()?;
        Ok(status.success())
    }

    /// Full masquerade ruleset passes `nft -c -f` (syntax check ONLY — never
    /// applies any NAT rule or networking change).
    ///
    /// Tagged `#[ignore]` — CI runner may not have nft installed or may lack
    /// the kernel module for the check.  Run manually to confirm.
    #[test]
    #[ignore]
    fn nft_nat_ruleset_syntax_check_full() {
        if !nft_available() {
            eprintln!("nft not found — skipping NAT syntax check");
            return;
        }
        let ruleset = build_nat_ruleset("10.7.0.0/24", "eth0");
        assert!(
            nft_check_syntax(&ruleset).expect("nft invocation failed"),
            "nft -c rejected the NAT masquerade ruleset:\n{ruleset}"
        );
    }

    /// Different egress / subnet combinations all pass the syntax check.
    #[test]
    #[ignore]
    fn nft_nat_ruleset_syntax_check_variations() {
        if !nft_available() {
            return;
        }
        let cases = [
            ("10.7.0.0/24", "eth0"),
            ("192.168.100.0/24", "ens3"),
            ("172.16.0.0/12", "wlan0"),
        ];
        for (subnet, iface) in cases {
            let rs = build_nat_ruleset(subnet, iface);
            assert!(
                nft_check_syntax(&rs).expect("nft invocation failed"),
                "nft -c rejected ruleset for subnet={subnet} iface={iface}:\n{rs}"
            );
        }
    }

    /// An intentionally broken ruleset MUST be rejected, confirming the test
    /// actually invokes nft and is not trivially always-true.
    #[test]
    #[ignore]
    fn nft_nat_syntax_check_rejects_bad_ruleset() {
        if !nft_available() {
            return;
        }
        let bad = "table inet wg_gui_srv_nat { this is not valid !!! }";
        assert!(
            !nft_check_syntax(bad).expect("nft invocation failed"),
            "nft -c should have rejected the bad ruleset"
        );
    }
