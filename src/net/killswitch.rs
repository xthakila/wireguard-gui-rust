//! Kill switch — block all non-tunnel traffic via nftables while a tunnel is up.
//!
//! # Design
//!
//! The kill switch uses a **uniquely-named nftables table** (`inet wg_gui_killswitch`) so it
//! never collides with or alters any other firewall on the system.  The output chain has a
//! `policy drop` that blocks everything *except* the explicitly allowed paths listed below.
//!
//! ## Allow-list ordering (lockout-prevention rules MUST come first)
//!
//! 1. `oifname "lo"` — loopback always passes (local IPC, DNS-on-localhost, etc.)
//! 2. `oifname <wg-iface>` — outbound tunnel traffic itself is accepted
//! 3. `ip daddr <endpoint_ip> udp dport <endpoint_port>` — the WireGuard endpoint UDP so the
//!    daemon can reconnect even when the kill switch is armed; this is the punch-through hole
//!    that makes the dead-man-timer lease work correctly
//! 4. LAN/RFC1918 CIDRs (user-configurable) — keeps local network access live
//! 5. `ct state established,related` — return traffic for already-established sessions
//! 6. Optional per-app netns UDP punch-through — the netns-side endpoint UDP so per-app
//!    namespaces can still reach the WireGuard server
//!
//! Everything else hits the output chain's `policy drop`.
//!
//! ## Fail-safe: dead-man timer (lease)
//!
//! When the helper arms the kill switch it also schedules a `systemd-run` one-shot unit:
//!
//! ```text
//! systemd-run --on-active=<lease_secs>s \
//!             --unit=wg-gui-killswitch-reaper \
//!             --description="WireGuard GUI kill-switch lease reaper" \
//!             nft delete table inet wg_gui_killswitch
//! ```
//!
//! If the GUI process is killed (SIGKILL, crash, power loss) without disarming, the kernel
//! timer fires after `lease_secs` seconds and removes the blocking table automatically.  The
//! GUI renews the lease before it expires by issuing a fresh [`PrivCmd::KillSwitchArm`] (which
//! re-creates the table + timer atomically).
//!
//! ## Boot reconciliation
//!
//! On startup the GUI (or the helper, called once) checks for an orphaned `wg_gui_killswitch`
//! table with no live WireGuard interface present and removes it by calling
//! [`PrivCmd::KillSwitchDisarm`].
//!
//! ## Privilege boundary
//!
//! This module is **pure GUI-side**.  It builds [`PrivCmd`] values and constructs the nft
//! script text; it never applies any ruleset.  The actual `nft -f <file>` or
//! `nft delete table …` invocations happen inside the root helper binary.

use crate::error::AppResult;
use crate::net::privilege::{encode, run_privileged, PrivCmd};

// ─────────────────────────────────────────────────────────────────────────────
// Ruleset generation (pure, no side effects, golden-testable)
// ─────────────────────────────────────────────────────────────────────────────

/// The nftables table name used exclusively by this application.
///
/// Uniquely named so it never collides with any other firewall policy on the host.
pub const NFT_TABLE: &str = "inet wg_gui_killswitch";

/// Build the complete `nft -f` script that installs the kill-switch ruleset.
///
/// The returned string is a self-contained nftables script that:
/// - Flushes any pre-existing `wg_gui_killswitch` table (idempotent re-arm)
/// - Creates `table inet wg_gui_killswitch`
/// - Adds an `output` chain with `policy drop`
/// - Prepends the **lockout-prevention allow-list** in the correct priority order before the
///   implicit policy drop
///
/// # Parameters
///
/// - `iface` — the WireGuard interface name (e.g. `wg-gui-home`)
/// - `endpoint_ip` — the WireGuard server's public IP address
/// - `endpoint_port` — the WireGuard server's UDP port
/// - `lan_cidrs` — slice of LAN/RFC1918 CIDRs to keep reachable (may be empty)
/// - `netns_endpoint_udp` — optional `(ip, port)` for a per-app netns punch-through
///
/// # Guarantee
///
/// This function is **pure** and never executes any command.  Pass the output to a temp file
/// and invoke `nft -c -f <file>` (syntax check only) or let the helper run `nft -f <file>`.
pub fn build_ruleset(
    iface: &str,
    endpoint_ip: &str,
    endpoint_port: u16,
    lan_cidrs: &[&str],
    netns_endpoint_udp: Option<(&str, u16)>,
) -> String {
    let mut lines: Vec<String> = Vec::new();

    // Flush then recreate — makes re-arming idempotent without a separate disarm step.
    lines.push("# wg_gui_killswitch — generated by wireguard-gui-rust".to_string());
    lines.push("# DO NOT EDIT MANUALLY — managed by wireguard-gui-helper".to_string());
    lines.push(String::new());
    // Silently ignore the flush if the table does not exist yet (first arm).
    lines.push("add table inet wg_gui_killswitch".to_string());
    lines.push("delete table inet wg_gui_killswitch".to_string());
    lines.push(String::new());
    lines.push("table inet wg_gui_killswitch {".to_string());
    lines.push("    chain output {".to_string());
    lines.push("        type filter hook output priority 0;".to_string());
    lines.push("        policy drop;".to_string());
    lines.push(String::new());
    lines.push("        # ── lockout-prevention allow-list ──────────────────────────────".to_string());
    lines.push("        # Rule 1: loopback — always allow (IPC, local DNS, etc.)".to_string());
    lines.push("        oifname \"lo\" accept;".to_string());
    lines.push(String::new());
    lines.push("        # Rule 2: outbound tunnel traffic through the WireGuard interface".to_string());
    lines.push(format!("        oifname \"{iface}\" accept;"));
    lines.push(String::new());
    lines.push("        # Rule 3: WireGuard endpoint UDP — punch-through for (re)connect".to_string());
    lines.push(format!(
        "        ip daddr {endpoint_ip} udp dport {endpoint_port} accept;"
    ));
    lines.push(String::new());

    // Rule 4: LAN/RFC1918 CIDRs.
    if !lan_cidrs.is_empty() {
        lines.push("        # Rule 4: LAN/RFC1918 CIDRs".to_string());
        for cidr in lan_cidrs {
            lines.push(format!("        ip daddr {cidr} accept;"));
        }
        lines.push(String::new());
    } else {
        lines.push("        # Rule 4: LAN/RFC1918 CIDRs (none configured)".to_string());
        lines.push(String::new());
    }

    // Rule 5: established / related return traffic.
    lines.push("        # Rule 5: established / related return traffic".to_string());
    lines.push("        ct state established,related accept;".to_string());
    lines.push(String::new());

    // Rule 6: optional per-app netns UDP punch-through.
    if let Some((ns_ip, ns_port)) = netns_endpoint_udp {
        lines.push("        # Rule 6: per-app netns endpoint UDP punch-through".to_string());
        lines.push(format!(
            "        ip daddr {ns_ip} udp dport {ns_port} accept;"
        ));
        lines.push(String::new());
    } else {
        lines.push("        # Rule 6: per-app netns UDP punch-through (not configured)".to_string());
        lines.push(String::new());
    }

    lines.push("        # Everything else is dropped by the chain policy.".to_string());
    lines.push("    }".to_string());
    lines.push("}".to_string());
    lines.push(String::new()); // trailing newline

    lines.join("\n")
}

