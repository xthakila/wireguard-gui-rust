    // ── nft syntax check (requires nft, marks #[ignore] if absent) ───────────

    /// Check whether `nft` is available. Used to conditionally mark tests.
    fn nft_available() -> bool {
        Command::new("nft").arg("--version").output().is_ok()
    }

    /// Run `nft -c -f <file>` (syntax check ONLY — never applies anything) and return true iff
    /// exit status is 0.
    fn nft_check(ruleset: &str) -> std::io::Result<bool> {
        let tmp = write_temp_ruleset(ruleset);
        let status = Command::new("nft")
            .arg("-c")
            .arg("-f")
            .arg(tmp.path())
            .status()?;
        Ok(status.success())
    }

    /// Full ruleset with LAN CIDRs + netns punch-through passes nft syntax check.
