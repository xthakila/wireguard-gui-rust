    /// Write `content` to a uniquely-named temp file and return the path.
    /// The caller is responsible for deletion (handled by `tempfile::NamedTempFile`).
    fn write_temp_ruleset(content: &str) -> tempfile::NamedTempFile {
        use std::io::Write as _;
        let mut f = tempfile::NamedTempFile::new().expect("create temp file");
        f.write_all(content.as_bytes()).expect("write ruleset");
        f
    }

    // ── build_ruleset: golden-string ordering ─────────────────────────────────

    /// Rule 1 (lo) must precede Rule 2 (wgif), Rule 2 must precede Rule 3 (endpoint UDP),
    /// Rule 3 must precede Rule 5 (established), and the drop policy header must be present.
    #[test]
    fn allow_list_ordering_lo_then_wgif_then_endpoint_then_lan_then_established() {
        let ruleset = build_ruleset(
            "wg-gui0",
            "203.0.113.7",
            51820,
            &["192.168.0.0/16", "10.0.0.0/8"],
            None,
        );

        // The policy must be there.
        assert!(
            ruleset.contains("policy drop"),
            "missing 'policy drop' in:\n{ruleset}"
        );

        // Extract byte positions of each required token to verify ordering.
        let pos_lo = ruleset.find("oifname \"lo\" accept").expect("lo rule missing");
        let pos_wgif = ruleset
            .find("oifname \"wg-gui0\" accept")
            .expect("wgif rule missing");
        let pos_endpoint = ruleset
            .find("ip daddr 203.0.113.7 udp dport 51820 accept")
            .expect("endpoint rule missing");
        let pos_lan_first = ruleset
            .find("ip daddr 192.168.0.0/16 accept")
            .expect("first LAN rule missing");
        let pos_lan_second = ruleset
            .find("ip daddr 10.0.0.0/8 accept")
            .expect("second LAN rule missing");
        let pos_established = ruleset
            .find("ct state established,related accept")
            .expect("established rule missing");

        assert!(pos_lo < pos_wgif, "lo must come before wgif");
        assert!(pos_wgif < pos_endpoint, "wgif must come before endpoint");
        assert!(pos_endpoint < pos_lan_first, "endpoint must come before LAN");
        assert!(pos_lan_first < pos_lan_second, "first LAN before second LAN");
        assert!(pos_lan_second < pos_established, "LAN must come before established");
    }

    #[test]
    fn allow_list_ordering_without_lan() {
        let ruleset = build_ruleset("wg-gui1", "198.51.100.1", 13231, &[], None);

        let pos_lo = ruleset.find("oifname \"lo\" accept").expect("lo rule missing");
        let pos_wgif = ruleset
            .find("oifname \"wg-gui1\" accept")
            .expect("wgif rule missing");
        let pos_endpoint = ruleset
            .find("ip daddr 198.51.100.1 udp dport 13231 accept")
            .expect("endpoint rule missing");
        let pos_established = ruleset
            .find("ct state established,related accept")
            .expect("established rule missing");

        assert!(pos_lo < pos_wgif);
        assert!(pos_wgif < pos_endpoint);
        assert!(pos_endpoint < pos_established);
    }

    #[test]
    fn allow_list_ordering_with_netns_udp() {
        let ruleset = build_ruleset(
            "wg-gui0",
            "203.0.113.7",
            51820,
            &["10.0.0.0/8"],
            Some(("203.0.113.7", 51820)),
        );

        let pos_established = ruleset
            .find("ct state established,related accept")
            .expect("established missing");
        // The netns punch-through must appear AFTER established (rule 6).
        // We use the second occurrence of the endpoint accept line (the netns one).
        let first_ep = ruleset
            .find("ip daddr 203.0.113.7 udp dport 51820 accept")
            .expect("first endpoint missing");
        let second_ep = ruleset
            .rfind("ip daddr 203.0.113.7 udp dport 51820 accept")
            .expect("second endpoint missing");

        // There must be two occurrences (rule 3 + rule 6).
        assert_ne!(first_ep, second_ep, "expected two endpoint-udp rules");
        assert!(
            pos_established < second_ep,
            "established must precede netns punch-through"
        );
    }

