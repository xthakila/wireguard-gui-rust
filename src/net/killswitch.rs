/// Client-side kill-switch controller.
///
/// All methods are **pure builders or async dispatchers**.  The actual nft execution happens
/// only inside the root helper binary (via `pkexec`); this struct never runs privileged code.
pub struct KillSwitch;

impl KillSwitch {
    /// Arm the kill-switch for `iface`.
    ///
    /// Builds a [`PrivCmd::KillSwitchArm`] and dispatches it through the privileged helper.
    /// The helper will:
    /// 1. Verify `iface` is present (`ip link show <iface>`) — refuses if absent.
    /// 2. Write the ruleset to a temp file and run `nft -f <file>`.
    /// 3. Schedule the dead-man timer:
    ///    ```text
    ///    systemd-run --on-active=<lease_secs>s \
    ///                --unit=wg-gui-killswitch-reaper \
    ///                --description="WireGuard GUI kill-switch lease reaper" \
    ///                nft delete table inet wg_gui_killswitch
    ///    ```
    ///
    /// # Dead-man timer
    ///
    /// The `lease_secs` parameter bounds the maximum time the kill-switch stays active without
    /// a renewal.  The GUI must call `arm` again before `lease_secs` elapse to reset the timer;
    /// if the GUI is killed (SIGKILL, crash), the reaper unit fires automatically.
    pub async fn arm(
        &self,
        iface: &str,
        endpoint_ip: &str,
        endpoint_port: u16,
        lan_cidrs: Vec<String>,
        lease_secs: u64,
        netns_endpoint_udp: Option<(String, u16)>,
    ) -> AppResult<()> {
        let cmd = PrivCmd::KillSwitchArm {
            iface: iface.to_owned(),
            endpoint_ip: endpoint_ip.to_owned(),
            endpoint_port,
            lan_cidrs,
            lease_secs,
            netns_endpoint_udp,
        };
        run_privileged(&cmd).await
    }

    /// Disarm the kill-switch by removing the `wg_gui_killswitch` table.
    ///
    /// Builds a [`PrivCmd::KillSwitchDisarm`] and dispatches it.  The helper runs:
    /// `nft delete table inet wg_gui_killswitch`.
    ///
    /// Idempotent: if the table does not exist the helper should treat the nft error as a no-op.
    pub async fn disarm(&self) -> AppResult<()> {
        run_privileged(&PrivCmd::KillSwitchDisarm).await
    }

    /// Build the [`PrivCmd::KillSwitchArm`] payload **without dispatching it**.
    ///
    /// Use this for golden-string tests, dry-run previews, or anywhere the JSON wire shape
    /// must be inspected without executing anything.
    pub fn arm_cmd(
        iface: &str,
        endpoint_ip: &str,
        endpoint_port: u16,
        lan_cidrs: Vec<String>,
        lease_secs: u64,
        netns_endpoint_udp: Option<(String, u16)>,
    ) -> AppResult<String> {
        let cmd = PrivCmd::KillSwitchArm {
            iface: iface.to_owned(),
            endpoint_ip: endpoint_ip.to_owned(),
            endpoint_port,
            lan_cidrs,
            lease_secs,
            netns_endpoint_udp,
        };
        encode(&cmd)
    }

    /// Build the [`PrivCmd::KillSwitchDisarm`] payload without dispatching it.
    pub fn disarm_cmd() -> AppResult<String> {
        encode(&PrivCmd::KillSwitchDisarm)
    }
}

